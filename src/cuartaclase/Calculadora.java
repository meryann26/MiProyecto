public class Calculadora {
    public int sumar(int num1, int num2){
        int resultado;
        resultado = num1 + num2;
        return resultado;
    }
    public int restar(int num1, int num2){
        int resultado;
        resultado = num1 - num2;
        return resultado;
    }
    
}
